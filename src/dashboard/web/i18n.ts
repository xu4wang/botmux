const dictionaries: Record<DashboardLocale, DashboardMessages> = { zh, en };

export function normalizeDashboardLocale(value: unknown): DashboardLocale | null {
  if (typeof value !== 'string') return null;
  const lower = value.trim().toLowerCase();
  if (lower === 'zh' || lower.startsWith('zh-')) return 'zh';
  if (lower === 'en' || lower.startsWith('en-')) return 'en';
  return null;
}

export function detectDashboardLocale(languages: readonly string[] = []): DashboardLocale {
  for (const lang of languages) {
    const normalized = normalizeDashboardLocale(lang);
    if (normalized) return normalized;
  }
  return 'zh';
}

export function createDashboardTranslator(locale: DashboardLocale): (key: string, params?: Record<string, string | number>) => string {
  return (key, params) => {
    const template = dictionaries[locale][key] ?? dictionaries.zh[key] ?? key;
    if (!params) return template;
    return template.replace(/\{(\w+)\}/g, (_, name) => {
      const value = params[name];
      return value === undefined || value === null ? `{${name}}` : String(value);
    });
  };
}

export function readStoredDashboardLocale(storage: Storage | undefined, languages: readonly string[]): DashboardLocale {
  const stored = storage ? normalizeDashboardLocale(storage.getItem(DASHBOARD_LOCALE_STORAGE_KEY)) : null;
  return stored ?? detectDashboardLocale(languages);
}
